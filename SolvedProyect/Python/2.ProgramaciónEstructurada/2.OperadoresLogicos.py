#Operadores Logicos

numero_1 = 5
numero_2 = 4
numero_3 = 10



#Operadores Relacionales


#<
if(numero_1 > numero_2):
   print(numero_1, "Es mmayor que", numero_2)
#>
elif(numero_1 < numero_2):
   print(numero_1, "Es menor que", numero_2)
#<=
elif(numero_1 <= numero_2):
   print(numero_1, "Es menor igual", numero_2)

#>=
elif(numero_1 >= numero_2):
   print(numero_1, "Es mayor igual", numero_2)
#!=
elif(numero_1 != numero_2):
   print(numero_1, "Diferente", numero_2)
#==
elif(numero_1 == numero_2):
   print(numero_1, "Es igual", numero_2)

#Operadores Lógicas

#And
elif(numero_1 == numero_2 and numero_1 < numero_3):
   print(numero_1, " Es igual ", numero_2, " y " ,numero_1," Es menor a ", numero_3)

#Or
elif(numero_1 == numero_2 or numero_1 < numero_3):
   print(numero_1, " Es igual ", numero_2, " o " ,numero_1," Es menor a ", numero_3)

#Not
elif(not(numero_1 == numero_2) and not(numero_1 < numero_3)):
   print(numero_1, " Es igual ", numero_2, " y " ,numero_1," Es menor a ", numero_3)


